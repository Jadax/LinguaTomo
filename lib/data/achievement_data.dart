enum AchievementRewardType {
  nestItem,
  leoReaction,
  postcardStamp,
  profileStyle,
  trophy,
}

class AchievementSnapshot {
  const AchievementSnapshot({
    required this.missions,
    required this.postcards,
    required this.streak,
    required this.handwritingAttempts,
    required this.bestHandwriting,
    required this.grammarPlanted,
    required this.grammarReviews,
    required this.cultureEvidence,
    required this.interactionEvidence,
    required this.xp,
    this.festivalsCelebrated = 0,
    this.seasonsCelebrated = 0,
    this.summerFestivals = 0,
    this.winterFestivals = 0,
    this.nestItemsPlaced = 0,
  });

  final int missions;
  final int postcards;
  final int streak;
  final int handwritingAttempts;
  final int bestHandwriting;
  final int grammarPlanted;
  final int grammarReviews;
  final int cultureEvidence;
  final int interactionEvidence;
  final int xp;

  /// Distinct festival windows experienced inside the app.
  final int festivalsCelebrated;

  /// Distinct seasons (spring, summer, autumn, winter) with a celebration.
  final int seasonsCelebrated;

  /// Distinct festivals celebrated during June, July or August.
  final int summerFestivals;

  /// Distinct festivals celebrated during December, January or February.
  final int winterFestivals;

  /// Memory objects currently placed inside the Nest.
  final int nestItemsPlaced;
}

class AchievementDefinition {
  const AchievementDefinition({
    required this.id,
    required this.title,
    required this.emoji,
    required this.requirement,
    required this.rewardType,
    required this.reward,
    required this.progress,
    required this.target,
    this.secret = false,
  });

  final String id;
  final String title;
  final String emoji;
  final String requirement;
  final AchievementRewardType rewardType;
  final String reward;
  final int Function(AchievementSnapshot) progress;
  final int target;
  final bool secret;

  bool unlocked(AchievementSnapshot snapshot) => progress(snapshot) >= target;
}

