enum AchievementRewardType {
  nestItem,
  leoReaction,
  postcardStamp,
  profileStyle,
}

class AchievementSnapshot {
  const AchievementSnapshot({
    required this.missions,
    required this.postcards,
    required this.streak,
    required this.handwritingAttempts,
    required this.bestHandwriting,
    required this.grammarPlanted,
    required this.grammarReviews,
    required this.cultureEvidence,
    required this.interactionEvidence,
    required this.xp,
  });

  final int missions;
  final int postcards;
  final int streak;
  final int handwritingAttempts;
  final int bestHandwriting;
  final int grammarPlanted;
  final int grammarReviews;
  final int cultureEvidence;
  final int interactionEvidence;
  final int xp;
}

class AchievementDefinition {
  const AchievementDefinition({
    required this.id,
    required this.title,
    required this.emoji,
    required this.requirement,
    required this.rewardType,
    required this.reward,
    required this.progress,
    required this.target,
    this.secret = false,
  });

  final String id;
  final String title;
  final String emoji;
  final String requirement;
  final AchievementRewardType rewardType;
  final String reward;
  final int Function(AchievementSnapshot) progress;
  final int target;
  final bool secret;

  bool unlocked(AchievementSnapshot snapshot) => progress(snapshot) >= target;
}

